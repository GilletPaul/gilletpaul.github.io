$("#show-content1").click(function () {
    if ($(".content").css("width") == "700px") {
        if ($("#hider12").css("display") === "block") {
            $("#hider12").hide();
        } else {
            $("#hider22").hide();
            $("#hider3").hide();
            $("#hider42").hide();
            $("#hider52").hide();
            $("#hider6").hide();
            $("#hider12").show();
        }
    } else {
        if ($("#hider1").css("display") === "block") {
            $("#hider1").hide();
        } else {
            $("#hider2").hide();
            $("#hider3").hide();
            $("#hider4").hide();
            $("#hider5").hide();
            $("#hider6").hide();
            $("#hider1").show();
        }
    }
});

